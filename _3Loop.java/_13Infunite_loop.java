public class _13Infunite_loop {
    public static void main(String[] args) {
        // Infinite loop using for loop
        for (;;) {
            System.out.println("This loop will run forever.");
        }

    }
}

/*Common Ways to Create Infinite Loops
1️⃣ Infinite while Loop
while (true) {
    System.out.println("Infinite loop");
}


2️⃣ Infinite for Loop
for (;;) {
    System.out.println("Infinite loop");
}


3️⃣ Infinite do-while Loop
do {
    System.out.println("Infinite loop");
} while (true);


4️⃣ Condition Never Changes
int i = 1;
while (i > 0) {   // i never changes
    System.out.println(i);
}

*/