public class _1Loop {
    public static void main(String[] args) {
        for (int i=0; i<=6; i++){
            System.out.println("Sohel is print for " + i +" times");
        }
        System.out.println("\n\n\n");
        
        for (int sohel=6; sohel>=0; sohel--){
            System.out.println("Sohel is print for " + sohel +" times");
        }
    }
}
