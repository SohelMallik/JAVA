public class _11Increment_Decrement {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x++); // Print 5, then x=6
        System.out.println(x);// Print 6
        
        int y=5;
        System.out.println(++y); // y=6, then Print 6
        System.out.println(y); // Print 6

        int z=10;
        int p=z++; // p=10, then z=11
        System.out.println(z+" "+p); // Print 11 10

      
    }
}
